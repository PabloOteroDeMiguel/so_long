#include"libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*aux;

	i = 0;
	aux = lst;
	while (aux != NULL)
	{
		i++;
		aux = aux->next;
	}
	return (i);
}
